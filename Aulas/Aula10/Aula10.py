# Aula10 - IF, ELIF, ELSE e Booleans.

'''
Condições: IF, ELIF e ELSE:
Ex:
'''

# If - Se

if True:
    print("Verdadeiro. ")
# Else - Senão

if True:
    print("Verdadeiro ")
else:
    print("Falso ")

# ElIf - Senão se

if False:
    print("Verdadeiro. ")
elif True:
    print("Agora é verdadeiro. ")
else:
    print("Falso. ")

