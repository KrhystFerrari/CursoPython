# Aula06 - Operadores Aritméticos

'''
+ = Adição;
- = Subtração;
* = Multiplicação;
/ = Divisão;
// = Divisão inteira (resultado arredondado);
** = Exponênciação;
% = Resto da divisão;
() = Alterador de precêdentes em expressões númericas.
'''

print(1 + 1)  # Adição
print(1 - 1)  # Subtração
print(10 * 10)  # Multiplicação
print(10 / 5)  # Divisão
print(10 // 2)  # Divisão inteira
print(5 ** 2)  # Exponênciação
print(9 % 2)  # Resto da divisão
print((10 + 10) / 2)  # Expressão com precedentes

